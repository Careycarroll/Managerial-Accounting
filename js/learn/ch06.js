// ── Tool 3: Responsibility Center Classifier ──────────────────

function initRCClassifier() {
  const scenarioEl   = document.getElementById('rc-scenario');
  const feedbackEl   = document.getElementById('rc-feedback');
  const counterEl    = document.getElementById('rc-counter');
  const prevBtn      = document.getElementById('rc-prev');
  const nextBtn      = document.getElementById('rc-next');
  const progressFill = document.getElementById('rc-progress-fill');
  const progressLbl  = document.getElementById('rc-progress-label');
  if (!scenarioEl) return;

  let current = 0;
  const answers = new Array(RC_SCENARIOS.length).fill(null);
  const correct = new Array(RC_SCENARIOS.length).fill(false);

  function render(index) {
    const item = RC_SCENARIOS[index];
    scenarioEl.textContent = item.scenario;
    feedbackEl.hidden      = true;
    counterEl.textContent  = `${index + 1} of ${RC_SCENARIOS.length}`;
    prevBtn.disabled       = index === 0;

    document.querySelectorAll('.rc-btn').forEach(btn => {
      btn.classList.remove('rc-btn--correct', 'rc-btn--incorrect');
    });

    if (answers[index]) {
      const isCorrect  = answers[index] === item.answer;
      const userBtn    = document.querySelector(`.rc-btn[data-type="${answers[index]}"]`);
      const correctBtn = document.querySelector(`.rc-btn[data-type="${item.answer}"]`);
      if (userBtn)    userBtn.classList.add(isCorrect ? 'rc-btn--correct' : 'rc-btn--incorrect');
      if (!isCorrect && correctBtn) correctBtn.classList.add('rc-btn--correct');
      feedbackEl.hidden    = false;
      feedbackEl.className = `rc-feedback rc-feedback--${isCorrect ? 'success' : 'error'}`;
      feedbackEl.innerHTML = `<strong>${isCorrect ? '✓ Correct!' : '✗ Not quite.'}</strong> ${item.explanation}`;
      nextBtn.disabled     = index === RC_SCENARIOS.length - 1;
    } else {
      nextBtn.disabled = true;
    }
    updateProgress();
  }

  function updateProgress() {
    const count = correct.filter(Boolean).length;
    progressFill.style.width = `${Math.round((count / RC_SCENARIOS.length) * 100)}%`;
    progressLbl.textContent  = `${count} of ${RC_SCENARIOS.length} classified correctly`;
  }

  document.querySelectorAll('.rc-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      if (answers[current] && correct[current]) return;
      answers[current] = btn.dataset.type;
      if (btn.dataset.type === RC_SCENARIOS[current].answer) correct[current] = true;
      render(current);
    });
  });

  prevBtn.addEventListener('click', () => { if (current > 0) render(--current); });
  nextBtn.addEventListener('click', () => { if (current < RC_SCENARIOS.length - 1) render(++current); });
  render(0);
}

