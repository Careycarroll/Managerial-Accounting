// ── Tool 5: Kaizen Budget Simulator ──────────────────────────

function initKaizen() {
  const calcBtn   = document.getElementById('kaizen-calculate');
  const resultsEl = document.getElementById('kaizen-results');
  if (!calcBtn) return;

  calcBtn.addEventListener('click', () => calculateKaizen(resultsEl));

  initRandomizer('kaizen-randomize', [
    { id: 'kz-base-rate',    min: 5,      max: 100,    step: 1,    integer: false },
    { id: 'kz-base-qty',     min: 10000,  max: 500000, step: 10000,integer: true },
    { id: 'kz-improve-pct',  min: 0.5,    max: 5,      step: 0.25, integer: false },
    { id: 'kz-periods',      min: 2,      max: 8,      step: 1,    integer: true },
    { id: 'kz-target-rate',  min: 1,      max: 90,     step: 1,    integer: false },
  ], () => calculateKaizen(resultsEl));
}

function calculateKaizen(resultsEl) {
  const activity    = gs('kz-activity') || 'Activity';
  const driver      = gs('kz-driver')   || 'units';
  const baseRate    = gv('kz-base-rate');
  const baseQty     = gv('kz-base-qty');
  const improvePct  = gv('kz-improve-pct') / 100;
  const periods     = Math.round(gv('kz-periods'));
  const targetRate  = gv('kz-target-rate');

  const schedule = [];
  let rate = baseRate;
  const baseCost = baseRate * baseQty;

  for (let p = 1; p <= periods; p++) {
    if (p > 1) rate = rate * (1 - improvePct);
    const totalCost       = rate * baseQty;
    const savingsVsBase   = baseCost - totalCost;
    const savingsVsPrior  = p === 1 ? 0 : schedule[p-2].totalCost - totalCost;
    const cumSavings      = baseCost * p - schedule.slice(0, p-1).reduce((s, r) => s + r.totalCost, 0) - totalCost;
    schedule.push({ period: p, rate, totalCost, savingsVsBase, savingsVsPrior, cumSavings });
  }

  const targetMet = targetRate > 0 ? schedule.find(s => s.rate <= targetRate) : null;
  const finalRate = schedule[schedule.length - 1].rate;
  const totalSavings = schedule.reduce((s, r) => s + r.savingsVsBase, 0);

  const svgW = 500;
  const svgH = 160;
  const padL = 70, padR = 20, padT = 20, padB = 30;
  const chartW = svgW - padL - padR;
  const chartH = svgH - padT - padB;
  const maxCost = baseCost * 1.05;
  const barW    = chartW / periods * 0.6;
  const barGap  = chartW / periods;

  let svgBars = '';
  schedule.forEach((s, i) => {
    const barH  = (s.totalCost / maxCost) * chartH;
    const x     = padL + i * barGap + (barGap - barW) / 2;
    const y     = padT + chartH - barH;
    const color = targetRate > 0 && s.rate <= targetRate ? 'var(--color-success)' : 'var(--color-primary)';
    svgBars += `<rect x="${x}" y="${y}" width="${barW}" height="${barH}" fill="${color}" opacity="0.8" rx="2"/>`;
    svgBars += `<text x="${x + barW/2}" y="${svgH - padB + 14}" text-anchor="middle" font-size="10" fill="var(--color-text-muted)">P${s.period}</text>`;
    svgBars += `<text x="${x + barW/2}" y="${y - 4}" text-anchor="middle" font-size="9" fill="var(--color-text-muted)">${fmt(s.totalCost)}</text>`;
  });

  let targetLine = '';
  if (targetRate > 0) {
    const targetCost = targetRate * baseQty;
    const ty = padT + chartH - (targetCost / maxCost) * chartH;
    targetLine = `<line x1="${padL}" y1="${ty}" x2="${padL + chartW}" y2="${ty}" stroke="var(--color-success)" stroke-width="1.5" stroke-dasharray="5,3"/>`;
    targetLine += `<text x="${padL + chartW - 4}" y="${ty - 4}" text-anchor="end" font-size="9" fill="var(--color-success)">Target: ${fmt(targetRate)}/unit</text>`;
  }

  const yTicks = [0, 0.25, 0.5, 0.75, 1].map(t => {
    const cost = maxCost * t;
    const y    = padT + chartH - t * chartH;
    return `<text x="${padL - 6}" y="${y + 4}" text-anchor="end" font-size="9" fill="var(--color-text-muted)">${fmt(cost)}</text>
            <line x1="${padL}" y1="${y}" x2="${padL + chartW}" y2="${y}" stroke="var(--color-gray-100)" stroke-width="1"/>`;
  }).join('');

  const svg = `<svg viewBox="0 0 ${svgW} ${svgH}" style="width:100%;height:${svgH}px">
    ${yTicks}
    <line x1="${padL}" y1="${padT}" x2="${padL}" y2="${padT+chartH}" stroke="var(--color-gray-300)" stroke-width="1.5"/>
    <line x1="${padL}" y1="${padT+chartH}" x2="${padL+chartW}" y2="${padT+chartH}" stroke="var(--color-gray-300)" stroke-width="1.5"/>
    ${targetLine}
    ${svgBars}
  </svg>`;

  resultsEl.innerHTML = `
    <div class="kaizen-chart" style="margin-top:var(--space-5)">${svg}</div>
    <div style="overflow-x:auto;border-radius:var(--radius-lg);border:1px solid var(--color-border);margin-top:var(--space-4)">
      <table class="data-table">
        <thead>
          <tr>
            <th>Period</th>
            <th class="num">Rate ($/unit)</th>
            <th class="num">Total Cost</th>
            <th class="num">Savings vs Base</th>
            <th class="num">Savings vs Prior</th>
            <th class="num">Cumulative Savings</th>
          </tr>
        </thead>
        <tbody>
          <tr style="background:var(--color-gray-50);font-weight:600">
            <td>Base</td>
            <td style="text-align:right;font-family:var(--font-mono)">${fmtDec(baseRate)}</td>
            <td style="text-align:right;font-family:var(--font-mono)">${fmt(baseCost)}</td>
            <td style="text-align:right">—</td>
            <td style="text-align:right">—</td>
            <td style="text-align:right">—</td>
          </tr>
          ${schedule.map(s => `
            <tr style="${targetRate > 0 && s.rate <= targetRate ? 'color:var(--color-success);font-weight:600' : ''}">
              <td>Period ${s.period}${targetRate > 0 && s.rate <= targetRate && (!schedule[s.period-2] || schedule[s.period-2].rate > targetRate) ? ' ✓ Target Met' : ''}</td>
              <td style="text-align:right;font-family:var(--font-mono)">${fmtDec(s.rate)}</td>
              <td style="text-align:right;font-family:var(--font-mono)">${fmt(s.totalCost)}</td>
              <td style="text-align:right;font-family:var(--font-mono);color:var(--color-success)">${fmt(s.savingsVsBase)}</td>
              <td style="text-align:right;font-family:var(--font-mono);color:var(--color-success)">${s.period === 1 ? '—' : fmt(s.savingsVsPrior)}</td>
              <td style="text-align:right;font-family:var(--font-mono)">${fmt(s.cumSavings)}</td>
            </tr>
          `).join('')}
        </tbody>
      </table>
    </div>
    <div class="sensitivity-insight" style="margin-top:var(--space-4)">
      <strong>${activity}:</strong> Starting at ${fmtDec(baseRate)}/${driver}, a ${(improvePct*100).toFixed(2)}% improvement per period
      reduces the rate to ${fmtDec(finalRate)}/${driver} by Period ${periods} --
      a ${fmtPct((baseRate - finalRate) / baseRate)} total reduction.
      Total savings over ${periods} periods: <strong>${fmt(totalSavings)}</strong>.
      ${targetMet ? `Target rate of ${fmtDec(targetRate)}/${driver} is <strong style="color:var(--color-success)">achieved in Period ${targetMet.period}</strong>.` : targetRate > 0 ? `Target rate of ${fmtDec(targetRate)}/${driver} is <strong style="color:var(--color-danger)">not reached</strong> within ${periods} periods.` : ''}
    </div>
  `;

  const sw = getOrCreate('kaizen-show-work', 'div', '', resultsEl);
  renderShowWork(sw, [
    {
      label:   'Period 1 Rate',
      formula: 'Rate = Base Rate (no improvement in first period)',
      values:  `${fmtDec(baseRate)}/${driver}`,
      result:  fmtDec(schedule[0].rate) + '/' + driver,
    },
    {
      label:   'Period 2 Rate',
      formula: 'Rate = Prior Rate × (1 − Improvement %)',
      values:  `${fmtDec(schedule[0].rate)} × (1 − ${(improvePct*100).toFixed(2)}%)`,
      result:  fmtDec(schedule[1]?.rate || 0) + '/' + driver,
    },
    {
      label:   `Period ${periods} Rate`,
      formula: 'Rate = Base Rate × (1 − Improvement %)^(Periods − 1)',
      values:  `${fmtDec(baseRate)} × (1 − ${(improvePct*100).toFixed(2)}%)^${periods - 1}`,
      result:  fmtDec(finalRate) + '/' + driver,
      highlight: true,
    },
    {
      label:   'Total Savings',
      formula: 'Total Savings = Sum of (Base Cost − Period Cost) for each period',
      values:  `${periods} periods × average savings per period`,
      result:  fmt(totalSavings),
      highlight: true,
      note:    'Kaizen budgeting motivates continuous small improvements rather than one-time cost cuts. The cumulative effect compounds over time.',
    },
  ], { title: 'Show Work -- Kaizen Budget Calculations' });
}

